// DEFINE SPRITE CLASS FOR GAME ELEMENTS
// Description:
// This commit introduces a new class called Sprite, which is designed to manage
// animated sprites within a canvas environment. The Sprite class includes a constructor
//  function that initializes properties such as position, velocity, image, frames, and
// sprites. It also provides a draw method to render the sprite on the canvas. The draw
// method handles the animation logic by updating the frame index based on elapsed time,
// allowing for smooth animation playback.

class Sprite {
  constructor({
    position,
    velocity,
    image,
    frames = { max: 1 },
    sprites = [],
  }) {
    this.position = position;
    this.image = image;
    this.frames = { ...frames, val: 0, elapsed: 0 };
    this.image.onload = () => {
      this.width = this.image.width / this.frames.max;
      this.height = this.image.height / this.frames.max;
    };
    this.moving = false;
    this.sprites = sprites;
  }

  draw() {
    context.drawImage(
      this.image,
      this.frames.val * this.width,
      0,
      this.image.width / this.frames.max,
      this.image.height,
      this.position.x,
      this.position.y,
      this.image.width / this.frames.max,
      this.image.height
    );
    if (!this.moving) return;
    if (this.frames.max > 1) {
      this.frames.elapsed++;
    }
    if (this.frames.elapsed % 10 === 0) {
      if (this.frames.val < this.frames.max - 1) this.frames.val++;
      else this.frames.val = 0;
    }
  }
}

